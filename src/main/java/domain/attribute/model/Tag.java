package domain.attribute.model;

public class Tag {
	private long id;
	private String name;
	private String label;
	
	public Tag(long id, String name, String label) {
		this.id = id;
		this.name = name;
		this.label = label;
	}
	
	public Tag() {
		this(0, "", "");
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
	
}
