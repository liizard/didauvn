package domain.attribute.model;

public class PlaceTag {
	
	private long id;
	private String name;
	
	public PlaceTag(long id, String name) {
		this.id = id;
		this.name = name;
	}
	
	public PlaceTag() {
		this(0,"");
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
